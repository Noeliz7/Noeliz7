from turtle import Turtle

def draw_h():
    h = Turtle()
    h.color("purple")
    h.fillcolor("yellow")

    h.pensize(5)

    h.pendown()
    h.penup()
    h.forward(20)

    h.pendown()
    h.penup()
    #n.hideturtle()
      
      







if __name__ == "__main__":
    draw_h()
    exitonclick()
    


